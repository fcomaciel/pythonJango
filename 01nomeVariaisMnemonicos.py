#!/usr/bin/env python3

'''
1. Exercício nomes de variáveis Mnemônicos
=======================================
Escreava um programa que calcule as horas trabalhada de um Freelance
e imprima na tela o total a ser pago.

Base para cálculo:
Horas trabalhada - 35
Valor da hora trabalhada: R$ 12.50
'''

horasTrabalhada = 35.0                              # Horas
valorPorHora = 12.5                                 # Valor hora trabalhada
totalPagamento = horasTrabalhada * valorPorHora     # Valor pagamento

print(totalPagamento)



